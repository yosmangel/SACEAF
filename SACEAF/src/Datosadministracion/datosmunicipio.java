package Datosadministracion;


public class datosmunicipio {

    String serial;
    String nombre;
    
    public datosmunicipio(String serial, String nombre){
        this.serial=serial;
        this.nombre=nombre;

    }

    public String getSerial() {
        return serial;
    }

    public String getNombre() {
        return nombre;
    }

  
}
