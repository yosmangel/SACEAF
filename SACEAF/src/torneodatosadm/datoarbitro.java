
package torneodatosadm;


public class datoarbitro {
String cedula;
String nombre;

    public datoarbitro(String cedula, String nombre) {
        this.cedula = cedula;
        this.nombre = nombre;
    }

    public String getCedula() {
        return cedula;
    }

    public String getNombre() {
        return nombre;
    }


}
