
package torneodatoscronograma;


public class datomodificarrondas {
    
    int nrondas;
    String idcompetencia;

    public datomodificarrondas(int nrondas, String idcompetencia) {
        this.nrondas = nrondas;
        this.idcompetencia = idcompetencia;
    }

    public int getNrondas() {
        return nrondas;
    }

    public String getIdcompetencia() {
        return idcompetencia;
    }
    
    
    
}
